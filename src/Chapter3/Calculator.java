package Chapter3;

public class Calculator {
    public double division(double firstNum, double secondNum){
        double result = firstNum / secondNum;
        return result;
    }
    public double addision(double firstNum, double secondNum){
        double result = firstNum + secondNum;
        return result;
    }
    public double maltiplycation(double firstNum, double secondNum){
        double result = firstNum * secondNum;
        return result;
    }

    public double suptraction(double firstNum, double secondNum){
        double result = firstNum - secondNum;
        return result;
    }
}
